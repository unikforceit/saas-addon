		);

		$this->end_controls_section();

	}


	/**
	 * Render image widget output on the frontend.
	 *
	 * Written in PHP and used to generate the final HTML.
	 *
	 * @since 1.0.0
	 * @access protected
	 */
	protected function render() {
		$settings = $this->get_settings_for_display();
        $gallery = get_listing_meta('mobile_listing_gallery');
        $placeholder = \Elementor\Utils::get_placeholder_image_src();
        $array = explode(',', $gallery);
        ?>
        <div class="saas-post-gallery elementor-image">
            <?php foreach ($array as $item){;?>
                <div class="gallery-item-saas">
                    <a href="<?php echo wp_get_attachment_image_url($item, 'full');?>">
                    <?php echo wp_get_attachment_image($item, 'full');?>
                    </a>
                </div>
            <?php }?>
        </div>
        <?php

	}

	public function get_meta_gallery(){

        $gallery = get_listing_meta('mobile_listing_gallery');
        $array = explode(',', $gallery);
        $list = [];

        foreach ($array as $item){
            $list[$item] = basename( get_attached_file( $item ) );
        }

        return $list;
    }
